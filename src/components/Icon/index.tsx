export type IconName = keyof typeof icons

type IconType = 'outline' | 'solid'

export interface IconProps extends SVGProps<SVGElement> {
  name: IconName
  size?: number
  type?: IconType
  className?: string
  viewBox?: string
  onClick?: (event: React.MouseEvent<SVGElement>) => any
  fallback?:
    | boolean
    | React.ReactChild
    | React.ReactFragment
    | React.ReactPortal
    | null
}

const Icon = ({
  name,
  type = 'solid',
  className = '',
  size = 24,
  fallback = undefined,
  onClick,
}: IconProps): JSX.Element => {
  if (!name) {
    throw new Error("Can't call Icon component without name props")
  }

  const Icon = React.useMemo(
    () =>
      React.lazy(() => import(`@heroicons/react/${type}/${icons[name]}Icon`)),
    [type, name, icons]
  )

  return (
    <React.Suspense
      fallback={
        fallback ?? (
          <div
            className={`${className ?? ''}`}
            style={{ height: size, width: size, display: 'inline' }}
          />
        )
      }
    >
      <Icon
        onClick={onClick}
        height={size}
        width={size}
        className={`${className}`}
      />
    </React.Suspense>
  )
}

Icon.defaultProps = {
  type: 'solid',
  size: 24,
  fallback: undefined,
}

export default Icon
